/**
 * @returns {String}
 */
export const capitalizeFirstLetterz = (string) => {
    const a = string;

    const b = a + " hi";

    return a + b;
};

/**
 * @param {String} string - string to capitalize
 * @returns {fail}
 */
export const capitalizeFirstLetta = (string) => {
    const a = string;
    const b = a + " hi";

    return a + b;
};
